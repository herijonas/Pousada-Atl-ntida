function hideCookies () {
    document.querySelector('.cookies').style.display = 'none' 
}

function negarCookies () {
    document.querySelector('.cookies-gerenciados').style.display = "flex"
}

function hideAcceptCookies () {
    document.querySelector('.cookies').style.display = 'none' 
    document.querySelector('.cookies-gerenciados').style.display = "none"
}